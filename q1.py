def heuristic(neighbour, goal_locations):
    # return the heuristic value for the neighbour
    # the heuristic value is the distance between the neighbour and the closest goal location
    min_distance = 100000000000000000000000
    for goal in goal_locations:
        # calculate the distance between the neighbour and the goal location using the euclidean distance
        distance = ((neighbour.lat - goal.lat) ** 2 + (neighbour.lon - goal.lon) ** 2) ** 0.5
        if distance < min_distance:
            min_distance = distance
    return min_distance


# a star algorithm for few starting locations and goal locations
def a_star(starting_locations, goal_locations):
    # initialize the frontier and explored sets
    frontier = []
    explored = []
    pathes = []
    # add the starting locations to the frontier
    for county in starting_locations:
        frontier.append(county)
        county.cost = 0
        county.heuristic = 0
        county.parent = None
    # loop until the frontier is empty
    while frontier:
        # get the county with the lowest cost
        current_county = frontier[0]
        for county in frontier:
            if county.cost + county.heuristic < current_county.cost + current_county.heuristic:
                current_county = county
        frontier.remove(current_county)

        # add the county to the explored set
        explored.append(current_county)
        # add the neighbours of the current county to the frontier
        for neighbour in current_county.neighbours:
            if neighbour not in explored and neighbour not in frontier:
                frontier.append(neighbour)
                # when checked the max distance between the counties (using findMaxdistance), the max distance was 356 so I used it as the cost between the counties
                # that will make sure that the huristic is admissible!
                neighbour.cost = current_county.cost + 356
                neighbour.heuristic = heuristic(neighbour, goal_locations)
                neighbour.parent = current_county

        # check if the current county is in the goal locations
        if current_county in goal_locations:
            # get the path from the current county to the starting location and add it to the pathes list
            path = []
            while current_county:
                path.insert(0, current_county)
                current_county = current_county.parent
            pathes.append(path)
            # if the pathes length is equal to the starting locations length then return the pathes
            if len(pathes) == len(starting_locations):
                return pathes

    # if the frontier is empty, then at list one path wasn't found
    return pathes


# hill climbing algorithm for few starting locations and goal locations
def hill_climbing(starting_locations, goal_locations, max_iterations=5):
    # initialize the best paths
    best_paths = []
    # loop for the max iterations
    for _ in range(max_iterations):
        current_paths = []
        # loop for the starting locations
        for start in starting_locations:
            current = start
            path = [current]
            # loop until the current county is in the goal locations
            while current not in goal_locations:
                best_neighbor = None
                best_heuristic = float('inf')
                # get the best neighbor for the current county
                for neighbor in current.neighbours:
                    if neighbor not in path:
                        h = heuristic(neighbor, goal_locations)
                        if h < best_heuristic:
                            best_neighbor = neighbor
                            best_heuristic = h
                # if the best neighbor is None then break the loop
                if best_neighbor is None:
                    break
                # add the best neighbor to the path
                current = best_neighbor
                path.append(current)
            # add the path to the current paths
            current_paths.append(path)
        # check if the current paths is better than the best paths
        if not best_paths or len(current_paths) > len(best_paths):
            best_paths = current_paths
        # if all the paths are in the goal locations then break the loop
        if all(path[-1] in goal_locations for path in best_paths):
            break

        # Restart from adjacent states of the original starting points
        starting_locations = [random.choice(start.neighbours) for start in starting_locations]

    return best_paths


# local beam search algorithm for few starting locations and goal locations
def local_beam_search(starting_locations, goal_locations, k=3):
    # initialize the beams and bags
    finished_beams = []
    bags = []
    # loop for the starting locations
    for start in starting_locations:
        beams = [[start]]
        bag = []
        # loop until the beams is empty or the goal locations is found
        while beams:
            new_beams = []
            # loop for the beams
            for beam in beams:
                current = beam[-1]
                # check if the current county is in the goal locations
                if current in goal_locations:
                    finished_beams.append(beam)
                    bags.append(bag)
                    break
                # get the neighbors of the current county
                neighbors = []
                for neighbor in current.neighbours:
                    if neighbor not in beam:
                        new_beam = beam + [neighbor]
                        neighbors.append((new_beam, heuristic(neighbor, goal_locations)))
                # sort the neighbors by the heuristic value and get the top k neighbors
                neighbors.sort(key=lambda x: x[1])
                top_k_neighbors = neighbors[:k]
                new_beams.extend(neighbor[0] for neighbor in top_k_neighbors)
                # add to the bag the most k promising neighbors and their heuristic values
                bag.append([(neighbor[0][-1].name, neighbor[0][-1].code, neighbor[1]) for neighbor in top_k_neighbors])
            # set the new beams and add the bag to the bags
            beams = new_beams

    return finished_beams, bags


def simulated_annealing(starting_locations, goal_locations):
    paths = []
    more_info_per_path = []
    for start in starting_locations:
        current = start
        path = [current]
        more_info = []
        T = 1  # Initial temperature
        T_min = 0  # Minimum temperature
        alpha = 0.95  # Cooling rate
        i = 0
        while current not in goal_locations and i < 100:
            neighbors = [neighbor for neighbor in current.neighbours if neighbor not in path]
            if not neighbors:
                break
            next_node = random.choice(neighbors)
            delta_e = heuristic(next_node, goal_locations) - heuristic(current, goal_locations)
            if delta_e < 0 or random.uniform(0, 1) < math.exp(-delta_e / T):
                current = next_node
                path.append(current)
                if delta_e < 0:
                    more_info.append(
                        f'{current.name}, {current.code}, delta_e: {delta_e} ,delta_e < 0 so 100% chance to choose it')
            else:
                more_info.append(
                    f'{current.name}, {current.code}, delta_e: {delta_e}, math.exp(-delta_e / T): {math.exp(-delta_e / T)}')
            if T > T_min:
                T *= alpha
            i += 1
    if current in goal_locations:
        paths.append(path)
        more_info_per_path.append(more_info)

    return paths, more_info_per_path


