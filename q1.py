def findMaxdistance():
    max_distance = 0
    for county in counties:
        for county1 in counties:
            distance = ((county.lat - county1.lat) ** 2 + (county.lon - county1.lon) ** 2) ** 0.5
            if distance > max_distance:
                max_distance = distance
    return max_distance


